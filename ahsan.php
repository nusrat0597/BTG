    <h1>Ahsan Manzil</h1>
</div>


<div class="lal des">

<p>Ahsan Manzil is the residence of Nawabs of Dhaka in Kumartuli area on the banks of Buriganga River and Sadar Kachari of Zamindari.Currently a museum.It is said that during the Mughal period, this place was the Rangamhal of Jamalpur pargana zamindar Sheikh Enayetullah.The French purchased Rangamahl from his son Matiullah and set up a trading post here.</p>
<br>
<p>In 1830 Khwaja Alimullah bought the Kuthir house from the French and made necessary renovations to make it his residence.Khawaja Abdul Ghani commissioned a European construction and engineering firm called Martin & Company to create a master plan for the Bhas Bhawan, with Ahsan Manzil as the main building.The construction of Ahsan Manzil started in 1859 and was completed in 1972.Abdul Ghani named the building Ahsan Manzil after his son Khwaja Ahsan Ullah.The newly constructed prasad building of that era was known as Rangamahl and the earlier building was known as Andaramahal.</p>
<br>
<p>Ahsan Manzil is a significant architectural landmark of the country.The two-storied prasad building, standing on a meter high altar, measures 125.4 meters in length and 28.5 square meters in width.The height from floor to ceiling is 5 meters on the first floor and 5.8 meters on the second floor.An auspicious grand open staircase leads down to the river bank from the double storey balcony over the one storey high carport on both the north-south sides of the palace.There was a fountain in the garden in front of the stairs which is not there now.Both the floors of the prasad have wide balconies with semi-circular arches on the north and south sides.The balconies and floors of the rooms are decorated with marble stones. </p>
<br>
<p>Realizing the historical and architectural importance of Ahsan Manzil, the Government of Bangladesh renovated the building and turned it into a museum.Its renovation work under the responsibility of the Directorate of Public Works and Architecture was completed in 1992 and  Bangladesh National Museum was constructed from 20 September that year and a museum was established here. </p>
</div>



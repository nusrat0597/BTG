</div>

<div class="historyp des">

<p> Hatirjheel project is located in the center of Dhaka.It started from Sonargaon Hotel in Carwan Bazaar and extended to Bansree.Tejgaon, Gulshan, Rampura, Badda, Bansree Niketan and Moghbazar surround the Khati Jheel.That is, Hatirjheel can be entered through each of the mentioned areas.</p>


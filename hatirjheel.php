<div class="pori bar">
	<h2>How to visit and explore Hatirjheel</h2>
</div>

<div class="porip des">

<p>You can come to Hatirjheel from any area of ​​Dhaka by bus or CNG or private car.Circular bus service is available to visit Hatirjheel project.Currently four 29-seater minibuses pick up and drop passengers from 10 stops around Hatirjheel.Tickets are available at six counters of Rampura, Madhubagh, FDC Mor, Boubazar ,Shooting Club and Merul Badda.The minimum fare from one counter to another counter is 10 taka.Rampura to Karwan Bazar will cost 15 taka.And you can see the whole Hatirjheel by bus only 30 taka.Daily bus service to Hatirjheel starts from 7 am and runs till 11 pm.Circular buses are not closed on public holidays or any festival.</p>


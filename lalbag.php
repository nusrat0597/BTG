<div class="history bar">
	<h2> History of Lalbagh Fort </h2>

</div>

<div class="historyp des">

<p> The construction of Lalbagh Fort started in 1678.The then Mughal Emperor Azam Shah started its construction.Although Azam Shah was the Mughal emperor for a short period of time.Yet within a short time he began his extraordinary work.Azam Shah was the son of Mughal Emperor Aurangzeb and the grandson of Emperor Shah Jahan,who is widely acclaimed in the world for building the Taj Mahal.About a year after the construction of this fort started, he had to leave for Delhi on the call of his father to suppress the Maratha rebellion there.After the departure of Emperor Azam Shah, the construction of the fort stopped temporarily.Then there was doubt whether the construction of this fort would be completed.But putting an end to all speculations, the then Nawab Shaista restarted the construction of Lalbagh Fort almost a year after the work had stopped.The construction of the fort continued with full vigor.However, almost four years after Shaista Khan resumed work, the construction of the fort stopped again,The construction of the fort was not resumed after that.Nawab Shaesta Khan's daughter Pari Bibi died, and Shaesta Khawa stopped the construction of Lalbagh Fort.After Pari Bibi's death, ironic ideas about the fort arose among everyone and everyone started thinking of the fort as a ghost.After Paribibi's death, he was buried inside the Lalbagh fort and henceforth known as Paribibi Samadhi.The dome that houses Paribibir's tomb was once gold-plated but is no longer so,The whole dome  is wrapped with copper sheet.</p>

</div>

<div class="pori bar">
	<h2>Tomb of Pari Bibi</h2>
</div>

<div class="porip des">

<p>This building is known as the Samadhi of Pari Bibi, the favorite daughter of Mughal Subedar Shaista Khan.In this unique building in Bangladesh, the interior nine rooms are decorated with marble stones, hard stones, and various colors of flowers and leaves.The roof of the rooms is made of hard stone.The artificial dome above the central chamber of the main mausoleum is covered with copper sheets.This 20.2 square meter tomb was built before 1688 AD.However, experts are of the opinion that Pari Bibi's body is not present here</p>

</div>

<div class="dekhar bar">
	<h2>All there is to see</h2>
</div>

<div class="dekharp des">

<p>Pari Bibi's mausoleum is always straight ahead when entering through the one of the three grand gates of Lalbagh Fort, which is currently open to the public.Often the picture of Lalbagh Fort seen in television news papers and magazines is actually a picture of Paribibir's tomb.There are three structures in the fort premises-</p><br>	
<p><b>1.Durbar Hall and Hammamkhana in the center.</b></p>
	<p><b>2.Tomb of Pari Bibi</b></p>
	<p><b>3.Shahi Mosque in the North West</b></p><br>
	<p>There is a mosque in the fort,before Azam Shah left for Delhi, he built this mosque.The three-domed mosque is able to catch anyone's eye.Prayers are offered in congregation in the mosque.There are very few such old mosques in Dhaka.Lalbagh Fort has several fountains here and there which are open only on special days like Eid.There are also tunnels in the fort. It is said that earlier it was possible to go through the tunnels, but now it is not possible to go through them.It should be noted that there was no truth in the hearsay of seeking the tunnel.The Lalbagh Fort houses a museum open to the public which was formerly the residence of Nawab Shaista Khan from where he controlled everything.The museum has a lot to see.Various hand-painted paintings of the Mughal period can be seen there which one cannot fail to be impressed by.Shaista Khan's useful things are kept there.Moreover, there are various weapons and costumes of that time in the common currency of that time and so on.
</p>



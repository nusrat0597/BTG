<p>This Mahera zamindar house is located in Tangail on the outskirts of Dhaka.Of all the horse-like places in Tangail, Maharaj Zamindar's house is the most beautiful.This Mahera Zamindar house is situated on 1174 percent land.It consists of three buildings.Excellent craftsmanship in each of the three installations.Seeing these crafts will fill your mind.</p><br>
<p>In front of the landlord's house there is a huge lake called Visakha Sagar.There are two picturesque gates to enter the house.Behind the building are Pashra and Rani ponds.There are beautiful flower gardens to enhance the beauty.On the southern side adjacent to the Visakha Sea is the huge Amara Kanan. And inside the house there are different kinds of birds kept in huge cages which will make anyone happy to see.</p><br>
<p>Before the 1990s, the manor house was modeled after the Spanish city of CÃ³rdoba.During the War of Independence, Pak forces attacked Mahera zamindar's house and brutally shot dead 5 villagers including the zamindar's daughter-in-law. Later, they left this country by boat on the Laujong River.A camp of the Liberation Army was established here.In 1972 this manor house was initiated as a Police Training School and in 1990 the Police Training School was converted into a Police Training Centre.</p><br>
<p>The place is an ideal place for a picnic.You can even go for a trip with family members.And if you want to visit, you can go anytime, you don't have to wait for the picnic season. </p>


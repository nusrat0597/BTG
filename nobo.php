<div class="lal des">

<p>Bangabandhu Sheikh Mujibur Rahman Novotheater aims to educate the people of the country, especially the new generation, to be scientifically minded and entertaining.In 1995, a plan was taken to set up a Novotheatre named after Bangabandhu Sheikh Mujibur Rahman in 1995 with the aim of imparting an idea about the planets and stars of space through entertainment.After the implementation of the project on 25 September 2004 by the Ministry of Science and Information and Communication Technology the Novotheater on 5.46 acres of land at Vijay Sarani Tejgaon was produced and renamed as Bhasani Novotheater after Maulana Abdul Hamid Khan Bhasani.12312.72 lakh rupees was spent on setting up the first state-of-the-art planetarium in the country.The architecture of Novotheater was designed by architect Alia Imam.State-of-the-art projection equipment was imported from Japan for the Novotheater shows.It is said to be one of the 12 hundred Novo theaters in the world.From 27 September 2004, the Novotheater opened to regular performances for the audience.Through the Bangabandhu Sheikh Mujibur Rahman Novotheatre Act 2010, Bhasani Novotheatre was renamed as Bangabandhu Sheikh Mujibur Rahman Novotheatre.</p>

